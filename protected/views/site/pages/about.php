<p><strong>General Order 165.</strong></br>United States: Utilities must patrol (walk, drive, or fly by) their systems once a year (in urban areas) or once every two years (in rural areas). Utilities must conduct detailed inspections every 3-5 years, depending on the type of equipment. For detailed inspections, utilities' records must specify the condition of inspected equipment, any problems found, and a scheduled date for corrective action. The utility must submit an annual report summarizing inspections made, equipment condition observed, and repairs made. Utilities are required to make intrusive inspections of power poles; no pole should go over 25 years before its first intrusive inspection, and once passed, every 20 years thereafter. Currently GO 165 is being studied for revisions to optimize the Commission's ability to identify areas on noncompliance with its safety standards.
